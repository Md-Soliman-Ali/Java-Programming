package Loop;

public class Problem_Solving_No_02 {

    public static void main(String[] args) {

        //print even numbers
        for (int i = 0; i <= 100; i += 2) {
            System.out.println(i);
        }

        //print odd numbers
        for (int i = 1; i <= 100; i += 2) {
            System.out.println(i);
        }

    }
}
